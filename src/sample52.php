<?php
    $numbers = [1, 2, "3", 4];

    if(in_array(3, $numbers, true)) {
        echo "3（整数）が見つかりました！";
    } else {
        echo "3（整数）は見つかりませんでした。";
    }
    ?>

※"3"のみ文字列であとは値

in_array(検索値, 配列, strictモード) で配列内を検索します。
第3引数が true のときは strict モード（型も一致する必要があります）。
ここでは整数の 3 を検索しています。

結果
配列内に文字列の "3" はありますが、整数の 3 はありません。
strict モードでは型が一致しないため、false になります。
出力: "3（整数）は見つかりませんでした。"

補足
第3引数を false（または省略）にすると、型変換が行われ、"3" が 3 と一致するため true になります。
このコードは、strict モードと通常モードの違いを示す例です。