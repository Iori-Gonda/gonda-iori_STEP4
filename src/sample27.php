<?php
$i = 0;

while ($i < 5) {
    echo "iの値は： $i\n";
    $i++;
}
/*
4行目: while($i >= 5) は、$i が 5 以上の間のみループします。
5行目: ループ内で $i の値を出力します。
6行目: $i++ で $i を 1 増やします。
問題: 2行目で $i = 0 のため、条件 $i >= 5 は初回から false となり、ループは実行されません。

\n の意味
\n は改行（エスケープシーケンス）です。
出力時、\n がある位置で改行されます。
ブラウザ表示では改行が見えない場合があります（HTMLでは改行は空白扱い）。コマンドラインやソース表示では改行として表示されます。
例: echo "iの値は： 5\n"; → 出力後に改行
*/
?>