<?php
    //①
    $fruits = ["りんご", "バナナ", "オレンジ"];
    foreach ($fruits as $fruit) {
        echo $fruit . "\n";
    }
    /*
    3行目：果物名の配列を作成
    4-6行目：foreachで各要素を順に処理
        配列の要素を1つずつ$fruitに代入
        要素を1行ずつ出力
    ①はインデックス配列の反復（値のみ）
    */

    //②
    $prices = ["りんご" => 100, "バナナ" => 50, "オレンジ"=> 70];
    foreach ($prices as $fruit => $price) {
        echo "$fruit の値段は $price 円です。\n";
    }
    /*
    ②の処理
    9行目：キーと値の連想配列を作成（果物名 => 価格）
    10-12行目：foreachでキーと値の両方を取得
        $fruitにキー（果物名）、$priceに値（価格）を代入
        文字列内で変数を展開して出力
    ②は連想配列の反復（キーと値の両方）
    */
?>