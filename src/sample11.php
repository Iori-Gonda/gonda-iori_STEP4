<?php
//ステップ1: $valueを5に初期化
$value = 5;  // この時点で $value = 5

//ステップ2: 前置インクリメント
//++$value: まず$valueを1増やしてから（5→6）、その値（6）を返す
//出力: 6
//この時点で $value = 6
echo ++$value;
echo "\n";

//ステップ3: 後置インクリメント
//$value++: 現在の値（6）を返してから、$valueを1増やす（6→7）
//出力: 6（現在の値）
//この時点で $value = 7（出力後に増加）
echo $value++;
echo "\n";

//前置デクリメント
//$valueは55
echo --$value;
echo "\n";

//後置デクリメント
//$valueは6(出力後に$valueは5になる)
echo $value--;
echo "\n";
?>

/*
つまり、後置インクリメントの時点では、その前に実行された前置インクリメントで $value が既に 6 になっています。
コードは上から順に実行されるため、その時点での $value の値が使われます。
単独で $value = 5; の後に $value++ を実行すれば、出力は 5、その後の $value は 6 になります。
*/